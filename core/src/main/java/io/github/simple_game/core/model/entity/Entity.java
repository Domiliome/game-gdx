package io.github.simple_game.core.model.entity;

import com.badlogic.gdx.math.Vector2;

/**
 * Базовый абстрактный класс для всех игровых объектов (сущностей) в игре.
 * Обеспечивает хранение пространственных координат объекта на карте
 * и задает общий жизненный цикл обновления логики.
 */
public abstract class Entity {
    /**
     * Двумерный вектор, хранящий текущую позицию объекта (X, Y) в игровом мире.
     */
    protected Vector2 position;

    /**
     * Создает новую игровую сущность в заданных координатах.
     *
     * @param x начальная координата X объекта на карте
     * @param y начальная координата Y объекта на карте
     */
    public Entity(float x, float y) {
        this.position = new Vector2(x, y);
    }

    /**
     * Абстрактный метод для обновления логики игрового объекта.
     * Вызывается автоматически каждый такт игрового цикла (кадр) из центрального цикла.
     *
     * @param deltaTime время, прошедшее с предыдущего кадра в секундах
     */
    public abstract void update(float deltaTime);

    /**
     * Возвращает текущие координаты расположения объекта в игровом мире.
     *
     * @return экземпляр {@link Vector2}, содержащий координаты X и Y сущности
     */
    public Vector2 getPosition() {
        return position;
    }
}
