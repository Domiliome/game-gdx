package io.github.simple_game.core.model.movement;

import io.github.simple_game.core.model.entity.Enemy;

/**
 * Интерфейс, определяющий контракт для реализации паттерна «Стратегия» (Strategy)
 * в логике перемещения вражеских юнитов.
 * Позволяет динамически менять алгоритмы движения объектов (например, ходьба по дорогам,
 * полет по прямой, плавание) без изменения самого класса врага.
 */
public interface MovementBehavior {

    /**
     * Вычисляет новые координаты положения врага на карте и смещает его
     * в зависимости от времени кадра и алгоритма конкретного поведения.
     *
     * @param enemy     объект врага, который выполняет перемещение в данный момент
     * @param deltaTime время, прошедшее с предыдущего кадра в секундах, для обеспечения
     *                  плавности движения независимо от частоты кадров (FPS)
     */
    void move(Enemy enemy, float deltaTime);
}
